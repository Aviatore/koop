CREATE FUNCTION public.list_for_packer(daysback integer) RETURNS TABLE(product_name character varying, products_in_baskets text)
    LANGUAGE plpgsql
    AS $$
BEGIN
    RETURN QUERY SELECT packer_list.product_name,
string_agg(
	packer_list.basket_name || ': ' || packer_list.quantity, ', ' 
	ORDER BY packer_list.basket_name) products_in_baskets
FROM (
	SELECT order_view.order_id, order_view.ordered_item_id, order_view."FirstName", order_view."LastName",
	order_view.product_name, order_view.order_start_date, order_view.order_stop_date, order_view.basket_name, 
	order_view.quantity, order_view.order_status_name 
	FROM order_view WHERE order_view.order_stop_date > NOW() - (daysback || ' days')::INTERVAL) AS packer_list
GROUP BY packer_list.product_name
ORDER BY packer_list.product_name;
END;
$$;


